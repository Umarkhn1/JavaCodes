package JavaCore;

public class DataType {
    public static void main(String[] args) {


        //butun sonlar
        byte a = 1;
        short b = 2;
        int c = 3;
        long d = 4;

        int result = a+b;
        System.out.println(result);

        //haqiqiy sonlar(действительные числа)
        float e = 2.1f;
        double f = 43.2;

        int r = (int)e+(int)f;
        double u = e + f;


        System.out.println(r);
        System.out.println(u);

        char g = '2';

        boolean h = false;

    }
}
