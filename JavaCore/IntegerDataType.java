package JavaCore;

public class IntegerDataType {

    public static void main(String[] args) {

        byte num = 1; //-127 128
        System.out.println(num);
        num=3;
        System.out.println(num);

        short num2= 20;
        System.out.println(num2);
        num2 = 20 + 13;
        System.out.println(num2);

        int num3 = 298_000_000;
        System.out.println(num3);
        num3 = num3/2;
        System.out.println(num3);

        long num4 = 2005L;
        System.out.println(num4);
        num4 = num+num2+num3+num4;
        System.out.println(num4);





    }
}
